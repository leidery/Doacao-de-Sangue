<h2>Quem somos</h1>
    <p>
        Nossa plataforma conecta doadores de sangue a hospitais e bancos de sangue que precisam de doações. Através
        deste
        site, os doadores podem se cadastrar, agendar doações e encontrar informações sobre os requisitos para doação
        de
        sangue, tais como idade, peso, histórico de saúde e medicações que possam afetar a doação.
    </p>
    <p>
        Através deste site, a doação de sangue torna-se mais acessível e conveniente, ajudando a salvar vidas e
        melhorar
        a
        saúde das pessoas. Com a ajuda desta plataforma, a doação de sangue pode ser feita com maior frequência e
        eficiência, aumentando assim o número de vidas salvas.
    </p>